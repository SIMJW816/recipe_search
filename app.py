# 4. 코사인 유사도 연산 함수
# ---------------------------------------------------------
def custom_cosine_similarity(vec1, vec2):
    """
    [수식 설명 및 동작 원리]
    두 텍스트의 임베딩 벡터 A와 B가 의미 공간상에서 얼마나 유사한 방향을 향하고 있는지 측정합니다.
    수식: Cosine Similarity = (A · B) / (||A|| * ||B||)
    
    1. np.dot(v1, v2)를 통해 두 벡터의 내적(Dot Product)을 구합니다.
    2. np.linalg.norm()을 사용하여 각 벡터의 크기(L2 Norm, 유클리드 길이)를 계산합니다.
    3. 내적을 두 벡터의 크기의 곱으로 나누어 코사인 각도를 산출합니다.
    4. 결과값은 -1(완전 반대)에서 1(완전 동일) 사이의 값을 가지며, 1에 가까울수록 의미론적으로 유사함을 의미합니다.
    """
    v1 = np.array(vec1)
    v2 = np.array(vec2)
    
    dot_product = np.dot(v1, v2)
    norm_v1 = np.linalg.norm(v1)
    norm_v2 = np.linalg.norm(v2)
    
    if norm_v1 == 0 or norm_v2 == 0:
        return 0.0
    return dot_product / (norm_v1 * norm_v2)

def verify_cosine_similarity(vec1, vec2):
    custom_sim = custom_cosine_similarity(vec1, vec2)
    scipy_sim = 1 - cosine(vec1, vec2)
    is_match = np.isclose(custom_sim, scipy_sim, atol=1e-6)
    return custom_sim, scipy_sim, is_match

