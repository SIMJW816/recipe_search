# [확장 기능] 키워드 기반 검색 (비교 분석용)
# ---------------------------------------------------------
def simple_keyword_search(query, df):
    """단순 띄어쓰기 기준으로 텍스트 매칭을 계산하여 반환하는 원시적 검색 함수"""
    keywords = query.split()
    scores = []
    for text in df['enriched_text']:
        # 텍스트에 포함된 키워드 개수를 카운트
        score = sum(1 for k in keywords if k in text)
        scores.append(score)
    
    df_kw = df.copy()
    df_kw['kw_score'] = scores
    return df_kw.sort_values(by='kw_score', ascending=False).head(3)

