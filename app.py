if submit_button:
    if query:
        with st.status("AI가 시간대와 영양 목적을 함께 분석하고 있습니다...", expanded=True) as status:
            st.write("1. 🧠 질의에서 '키워드', '시간대', '목적' 분리 중...")
            intent_data = extract_hybrid_intent(query)
            target_keyword = intent_data["keyword"]
            target_time = intent_data["meal_time"]
            target_purpose = intent_data["purpose"]
            
            st.write("2. 🧮 벡터 공간 탐색 및 하드 필터링(페널티) 적용 중...")
            query_embedding = get_embedding(target_keyword)
            
            similarities = []
            for idx, row in df.iterrows():
                sim = custom_cosine_similarity(query_embedding, row["embedding"])
                cat = row["category"]
                taste = row["taste"]
                
                # [로직 1] 시간대 페널티
                if target_time != "무관":
                    if target_time == "저녁" and "아침" in cat and "저녁" not in cat:
                        sim -= 0.3
                    elif target_time == "아침" and ("저녁" in cat or "야식" in cat):
                        sim -= 0.3
                    elif target_time == "야식" and "아침" in cat:
                        sim -= 0.3
                    if target_time in cat:
                        sim += 0.01 
                
                # [특수 필터 1] 맛에 대한 확고한 조건 제어
                spicy_triggers = ["매콤", "매운", "맵고", "맵게", "얼큰", "칼칼", "자극", "매워"]
                
                # 사용자의 검색어에 매운맛 관련 단어가 하나라도 포함되어 있다면
                if any(trigger in query for trigger in spicy_triggers):
                    # DB의 맛(taste) 설명에 아래 단어들이 하나도 없다면 가차없이 페널티
                    spicy_tastes = ["매콤", "매운", "얼큰", "불맛", "자극적", "칼칼", "매움", "맵고", "매워"]
                    if not any(word in taste for word in spicy_tastes):
                        sim -= 0.3
                
                # [특수 필터 2] '간단' 속성 제어
                if "간단" in query or "빠른" in query:
                    if "간편식" in cat or "간단식" in cat or "초간단" in row["name"]:
                        sim += 0.02

                # [로직 2] 목적 페널티
                if target_purpose == "고단백" or target_purpose == "다이어트":
                    if "분식" in cat or "야식" in cat or "면류" in cat or "간식" in cat or "디저트" in cat:
                        sim -= 0.2
                    if "고단백" in cat or "건강식" in cat or "다이어트" in cat:
                        sim += 0.1
                elif target_purpose == "해장":
                    if "해장" in cat or "국" in cat:
                        sim += 0.1
                    elif "간식" in cat or "디저트" in cat:
                        sim -= 0.2
                
                similarities.append(sim)
            
            df["similarity"] = similarities
            top_results = df.sort_values(by="similarity", ascending=False).head(5)
